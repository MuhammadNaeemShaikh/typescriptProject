const operations = {
    add: (val1, val2) => {
        return val1 + val2;
    },
    subtract: (val1, val2) => {
        return val1 - val2;
    },
    multiply: (val1, val2) => {
        return val1 * val2;
    },
    divide: (val1, val2) => {
        return val1 / val2;
    },
    mod: (val1, val2) => {
        return val1 % val2;
    }
};
export default operations;
